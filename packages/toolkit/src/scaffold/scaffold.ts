// Scaffold services — `new` / `init` / `clean` (cli.md CLI-PROD-NEW-001 +
// CLI-PROD-OUT-NEW-001, CLI-PROD-INIT-001 + CLI-PROD-EDGE-004, CLI-PROD-CLEAN-001;
// config-schema.md CFG-PROD-*; knowledge-pack.md KP-PROD-CONTENT-PLAYBOOK-001).
//
// These are the project-bootstrap verbs of the loop:
//   - `new <name>`  scaffolds a fresh project DIRECTORY that `zxs verify` PASSES out
//                   of the box (zx.config.json + an entry .asm that assembles and runs
//                   clean + a green tests/ smoke suite + the agent playbook).
//   - `init`        lays the same toolkit contract INTO an existing directory,
//                   non-destructively (idempotent; never clobbers a present file
//                   unless `--force`), reporting what it created vs skipped.
//   - `clean`       removes the generated artifacts (`build/` + `.zxs/`) so the next
//                   attempt starts clean; an absent dir is a success, not an error.
//
// W5 boundary (the clean-room seal): this slice emits a MINIMAL valid project — NOT
// the rich starter templates. The elaborate `game`/`platformer` starter contract (a
// lib/, a Makefile, the full knowledge pack, an installable package.json) belongs to
// `create-zx-vibes` + `starters/` (W5, which owns conformance row SCAFFOLD-VERIFY-001).
// `new` accepts `--template <game|platformer>` and records it as informational config
// metadata (CFG-PROD-FIELD-TEMPLATE-001), but the generated bytes are the same minimal
// project; the template-specific content is W5's job. Likewise the full knowledge pack
// is installed by `zxs setup` (Slice 11) — here we ship only the minimal agent playbook
// the scaffold itself mandates (KP-PROD-CONTENT-PLAYBOOK-001), not the whole pack.

import { existsSync, mkdirSync, rmSync, writeFileSync } from 'node:fs';
import { basename, dirname, isAbsolute, relative, resolve } from 'node:path';
import type { Command } from 'commander';
import {
  DEFAULT_ASSEMBLER,
  DEFAULT_ORG,
  DEFAULT_OUT_DIR,
  resolveConfig,
} from '../config/config.js';
import { successEnvelope, userError, type SuccessEnvelope } from '../output/envelope.js';
import { ZXS_DIR } from '../state/persist.js';
import type { CommandContext } from '../registry.js';

/** The default `--template` (CLI-PROD-NEW-001). */
export const DEFAULT_TEMPLATE = 'game';
/** The published package name recorded in a generated `zx.config.json` `toolkit` field. */
export const TOOLKIT_ID = '@zx-vibes/toolkit';
/** The conventional entry path a generated project configures (config-schema.md example). */
export const SCAFFOLD_ENTRY = 'src/main.asm';

// --- the minimal project content (clean-room: derived from this toolkit's own
//     proven-green verify/test fixtures, never from any oracle/W5 starter) --------

/**
 * The entry program: a HALT-synced 48K game loop. It assembles with the embedded
 * `@zx-vibes/asm`, sets the border (a port-0xFE write), enables IM 1 interrupts, and
 * idles in `halt`/`jr` — so a fresh 300-frame `run` reports `status:"ok"` with
 * `haltSynced:true` (no hang). This is exactly the shape `zxs verify` needs to pass.
 */
export const ENTRY_ASM = [
  '; Minimal zx-vibes entry point — a HALT-synced ZX Spectrum 48K game loop.',
  '; Assembles with the embedded @zx-vibes/asm and runs clean under `zxs verify`.',
  '        ORG 0x8000',
  'start:',
  '        ld sp, 0xFF00      ; a safe stack high in RAM',
  '        ld a, 2            ; border = red (also proves a port-0xFE write)',
  '        out (0xFE), a',
  '        ld a, 0x2A         ; a token byte...',
  '        ld (0x9000), a     ; ...stored so memEquals @0x9000 == 0x2A is provable',
  '        im 1               ; interrupt mode 1 (the ROM 50 Hz frame interrupt)',
  '        ei                 ; enable interrupts so HALT releases each frame',
  'main:',
  '        halt               ; wait for the frame interrupt (HALT-synced loop)',
  '        jr main',
  '',
].join('\n');

/**
 * The green smoke suite: one self-contained spec proving the toolchain end-to-end —
 * the program runs to `status:"ok"`, the loop is HALT-synced, and the border is the
 * expected colour. Its `smoke.asm` lives beside the spec in `tests/`, so the proof
 * stays green regardless of the project's own sources (the sandbox roots at the
 * project root, so a spec may also build `../src/…`, but this one deliberately does not).
 */
export const SMOKE_TEST_JSON = `${JSON.stringify(
  {
    build: 'smoke.asm',
    frames: 30,
    assert: [
      { type: 'status', equals: 'ok' },
      { type: 'haltSynced', equals: true },
      { type: 'borderColor', equals: 2 },
    ],
  },
  null,
  2,
)}\n`;

/** Ignore the generated artifacts `clean` removes. */
export const GITIGNORE = ['build/', '.zxs/', ''].join('\n');

